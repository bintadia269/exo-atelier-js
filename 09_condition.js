// Déclare une variable jour avec une valeur de 1 à 7
let jour = 4;

// Utilisation d'une structure pour afficher le jour correspondant
switch (jour) {
  case 1:
    alert('Lundi');
    break;
  case 2:
    alert('Mardi');
    break;
  case 3:
    alert('Mercredi');
    break;
  case 4:
    alert('Jeudi');
    break;
  case 5:
    alert('Vendredi');
    break;
  case 6:
    alert('Samedi');
    break;
  case 7:
    alert('Dimanche');
    break;
  default:
    alert('1  7.');
}
